from custom_components.escpos_printer.security import (
    MAX_PROCESSED_HEIGHT,
    MAX_SVG_ELEMENTS,
    MAX_SVG_SIZE_BYTES,
    MAX_SVG_USE_ELEMENTS,
    is_svg_bytes,
    validate_base64_image,
    validate_svg_bytes,
)

_MINIMAL_SVG = (
    b'<?xml version="1.0"?>\n'
    b'<svg xmlns="http://www.w3.org/2000/svg" width="32" height="32">'
    b'<rect width="32" height="32" fill="black"/></svg>'
)


# ---------------------------------------------------------------------------
# is_svg_bytes — first-bytes sniffing
# ---------------------------------------------------------------------------


class TestIsSvgBytes:
    @pytest.mark.parametrize(
        "raw",
        [
            _MINIMAL_SVG,
            b'<svg xmlns="http://www.w3.org/2000/svg"/>',
            b"  \n<?xml version='1.0'?><svg/>",
            b"\xef\xbb\xbf<svg/>",  # UTF-8 BOM
            b"<!-- a comment first --><svg/>",
        ],
    )
    def test_recognises_svg(self, raw: bytes) -> None:
        assert is_svg_bytes(raw) is True

    @pytest.mark.parametrize(
        "raw",
        [
            b"\x89PNG\r\n\x1a\n",  # PNG magic
            b"\xff\xd8\xff",  # JPEG SOI
            b"GIF89a",
            b"",
            b"not xml at all",
        ],
    )
    def test_does_not_recognise_raster(self, raw: bytes) -> None:
        assert is_svg_bytes(raw) is False

    @pytest.mark.parametrize(
        "raw",
        [
            # XHTML / HTML5 — root is <html>, not <svg>
            b'<?xml version="1.0"?><!DOCTYPE html><html><body/></html>',
            b'<!DOCTYPE html><html lang="en"><body/></html>',
            # RSS, Atom, RDF — common non-SVG XML payloads
            b'<?xml version="1.0"?><rss version="2.0"><channel/></rss>',
            b'<feed xmlns="http://www.w3.org/2005/Atom"><id>x</id></feed>',
            b'<rdf:RDF xmlns:rdf="http://www.w3.org/1999/02/22-rdf-syntax-ns#"/>',
            # Comment + non-SVG root
            b"<!-- copyright header --><html/>",
            # DOCTYPE without entities, then non-SVG root
            b'<?xml version="1.0"?><!DOCTYPE rss><rss/>',
        ],
    )
    def test_does_not_recognise_non_svg_xml(self, raw: bytes) -> None:
        # Tightened sniff (F1 / F-MED-6): generic XML must route through
        # the raster path so the user sees a meaningful Pillow error
        # rather than a misleading "Invalid or unsafe SVG" wrapper.
        assert is_svg_bytes(raw) is False

    def test_recognises_svg_after_doctype(self) -> None:
        # Inkscape historically emits `<!DOCTYPE svg PUBLIC ...>` ahead
        # of the root element. The sniff must accept this shape.
        raw = (
            b'<?xml version="1.0" encoding="UTF-8" standalone="no"?>\n'
            b'<!DOCTYPE svg PUBLIC "-//W3C//DTD SVG 1.1//EN" '
            b'"http://www.w3.org/Graphics/SVG/1.1/DTD/svg11.dtd">\n'
            b'<svg xmlns="http://www.w3.org/2000/svg" width="1" height="1"/>'
        )
        assert is_svg_bytes(raw) is True

    def test_recognises_svg_after_multiple_comments(self) -> None:
        # Some toolchains prepend multiple leading comments.
        raw = (
            b"<!-- copyright header -->"
            b"<!-- generated by tool -->"
            b'<svg xmlns="http://www.w3.org/2000/svg"/>'
        )
        assert is_svg_bytes(raw) is True

    def test_recognises_uppercase_svg_root(self) -> None:
        # Per XML 1.0, element names are case-sensitive; however SVG
        # exporters occasionally emit case-variant tags. The sniff is
        # case-insensitive because the downstream validate_svg_bytes
        # parses with defusedxml and confirms the actual root tag.
        assert is_svg_bytes(b"<SVG/>") is True

    def test_rejects_non_bytes(self) -> None:
        assert is_svg_bytes("not bytes") is False  # type: ignore[arg-type]


