def _fake_png_bytes(width: int = 32, height: int = 32) -> bytes:
    """Return PNG bytes for a solid-grey image at the requested size."""
    img = Image.new("L", (width, height), color=128)
    bio = io.BytesIO()
    img.save(bio, format="PNG")
    return bio.getvalue()


@contextmanager
def _stub_cairosvg(svg2png_impl: Any) -> Iterator[types.ModuleType]:
    """Replace ``cairosvg`` in :data:`sys.modules` with a stub.

    The real ``cairosvg`` dlopen's libcairo on import, which is absent on
    most macOS dev machines and on slim CI containers. Replacing the
    module before ``svg_renderer`` resolves its lazy import lets us
    exercise the full pipeline without the system library.
    """
    stub = types.ModuleType("cairosvg")
    stub.svg2png = svg2png_impl  # type: ignore[attr-defined]
    original = sys.modules.get("cairosvg")
    sys.modules["cairosvg"] = stub
    try:
        yield stub
    finally:
        if original is None:
            sys.modules.pop("cairosvg", None)
        else:
            sys.modules["cairosvg"] = original


class TestProcessImageFromBytesSvgHop:
    def test_svg_bytes_get_rasterised_then_decoded(self) -> None:
        captured: dict[str, object] = {}

        def fake_svg2png(**kwargs: Any) -> None:
            captured.update(kwargs)
            png = _fake_png_bytes(64, 32)
            kwargs["write_to"].write(png)

        with _stub_cairosvg(fake_svg2png):
            opts = ImageProcessOptions(width=384, profile_width=384)
            img = process_image_from_bytes(_MINIMAL_SVG, opts)

        assert captured["output_width"] == 384
        # output_height is pinned to MAX_PROCESSED_HEIGHT so a hostile
        # SVG declaring a huge declared height cannot trick cairo into
        # allocating an unbounded raster surface.
        assert captured["output_height"] == MAX_PROCESSED_HEIGHT
        # unsafe=False is what stops external-resource fetches in
        # cairosvg 2.9 (the parser auto-installs safe_fetch). Our code
        # must not be passing url_fetcher — that kwarg is not accepted
        # by cairosvg.svg2png().
        assert captured["unsafe"] is False
        assert "url_fetcher" not in captured
        assert img.mode == "1"

    def test_svg_validation_runs_before_cairosvg(self) -> None:
        calls: list[dict[str, Any]] = []

        def fake_svg2png(**kwargs: Any) -> None:
            calls.append(kwargs)

        with _stub_cairosvg(fake_svg2png):
            xxe = (
                b'<?xml version="1.0"?>\n'
                b'<!DOCTYPE foo [<!ENTITY xxe SYSTEM "file:///etc/passwd">]>\n'
                b'<svg xmlns="http://www.w3.org/2000/svg">&xxe;</svg>'
            )
            with pytest.raises(HomeAssistantError, match="Invalid or unsafe SVG"):
                process_image_from_bytes(xxe, ImageProcessOptions(width=384))
        assert calls == []

    def test_raster_image_path_unaffected(self) -> None:
        calls: list[dict[str, Any]] = []

        def fake_svg2png(**kwargs: Any) -> None:
            calls.append(kwargs)

        # PNG input must flow through the existing Pillow path without
        # invoking the cairosvg stub at all.
        with _stub_cairosvg(fake_svg2png):
            png = _fake_png_bytes(100, 50)
            opts = ImageProcessOptions(width=200)
            img = process_image_from_bytes(png, opts)
        assert img.mode == "1"
        assert calls == []

    def test_aspect_ratio_bomb_yields_bounded_surface(self) -> None:
        # F-T-MED-3 regression — the exact decompression-bomb payload
        # from the Phase-2 security report. The fix (F-HIGH-1) pinned
        # ``output_height=MAX_PROCESSED_HEIGHT`` in svg_renderer, so
        # cairosvg now allocates a bounded surface regardless of the
        # SVG's declared dimensions. This test asserts the kwargs we
        # send to cairosvg are still the bounded pair — the next
        # refactor that drops ``output_height`` would re-introduce
        # the OOM vector and trip this assertion.
        bomb = (
            b'<svg xmlns="http://www.w3.org/2000/svg" '
            b'width="1" height="999999">'
            b'<rect width="1" height="999999"/></svg>'
        )
        captured: dict[str, Any] = {}

        def fake_svg2png(**kwargs: Any) -> None:
            captured.update(kwargs)
            kwargs["write_to"].write(_fake_png_bytes(384, 384))

        with _stub_cairosvg(fake_svg2png):
            process_image_from_bytes(bomb, ImageProcessOptions(width=384))

        assert captured["output_width"] == 384
        assert captured["output_height"] == MAX_PROCESSED_HEIGHT
        assert captured["unsafe"] is False

    def test_memory_error_from_cairo_is_not_wrapped(self) -> None:
        # F-T-HIGH-2 regression — ``rasterise_svg_to_png`` deliberately
        # re-raises MemoryError / SystemError unchanged so the HA
        # process supervisor can react. A refactor that collapses to a
        # single ``except Exception`` would silently regress this.
        def fake_svg2png(**kwargs: Any) -> None:
            raise MemoryError("simulated cairo allocation failure")

        with _stub_cairosvg(fake_svg2png):
            with pytest.raises(MemoryError, match="simulated cairo"):
                process_image_from_bytes(
                    _MINIMAL_SVG, ImageProcessOptions(width=384)
                )

    def test_kill_switch_env_refuses_svg_before_render(
        self,
        monkeypatch: pytest.MonkeyPatch,
    ) -> None:
        # F-CI-MED-1: ``ESCPOS_DISABLE_SVG`` is the emergency
        # kill-switch for a hypothetical cairosvg / libcairo CVE.
        # The renderer must refuse before invoking cairosvg, so a
        # stub that records calls must NOT be called.
        calls: list[dict[str, Any]] = []

        def fake_svg2png(**kwargs: Any) -> None:
            calls.append(kwargs)

        monkeypatch.setenv("ESCPOS_DISABLE_SVG", "1")
        with _stub_cairosvg(fake_svg2png):
            with pytest.raises(HomeAssistantError, match="SVG rendering is disabled"):
                process_image_from_bytes(
                    _MINIMAL_SVG, ImageProcessOptions(width=384)
                )
        assert calls == [], "cairosvg must not be called when kill-switch is set"

    @pytest.mark.parametrize("value", ["0", "false", "no", "off", ""])
    def test_kill_switch_env_falsy_does_not_refuse(
        self,
        monkeypatch: pytest.MonkeyPatch,
        value: str,
    ) -> None:
        # The truthy-set is intentionally narrow so a stray
        # ``ESCPOS_DISABLE_SVG=`` doesn't break SVG support for users
        # who don't realise the var is set in their shell env.
        monkeypatch.setenv("ESCPOS_DISABLE_SVG", value)

        def fake_svg2png(**kwargs: Any) -> None:
            kwargs["write_to"].write(_fake_png_bytes(32, 32))

        with _stub_cairosvg(fake_svg2png):
            process_image_from_bytes(
                _MINIMAL_SVG, ImageProcessOptions(width=384)
            )


# ---------------------------------------------------------------------------
# Real-cairosvg integration test — guards against the kwarg-mismatch
# class of regression that the sys.modules stub silently swallows.
# Skipped automatically when libcairo (or cairosvg itself) is absent.
# ---------------------------------------------------------------------------


class TestRealCairosvgBinding:
    def test_real_cairosvg_renders_minimal_svg(self) -> None:
        """Exercise the actual cairosvg.svg2png kwarg surface.

        Regression guard for the kwarg-mismatch class of bug: the
        stub-based tests above accept ``**kwargs`` unconditionally, so
        ``cairosvg.svg2png(foo_bar=True)`` would silently pass against
        them. This test imports the real cairosvg (skipping if
        libcairo is unavailable on the host) and confirms our exact
        call signature is accepted end-to-end.
        """
        try:
            import cairosvg
        except (ImportError, OSError) as exc:
            # F-CI-CRIT-1 + F-T-HIGH-1: this test is the regression
            # guard for the cairosvg-API-mismatch class of bug found
            # during review. It MUST run in CI; locally it can skip on
            # dev boxes without libcairo. The CI=true env var is set in
            # validate.yml so a missing libcairo there is a hard error,
            # not a silent skip.
            if os.environ.get("CI") == "true":
                raise RuntimeError(
                    "CI must have libcairo installed so TestRealCairosvgBinding "
                    "runs — see validate.yml. Skip refused."
                ) from exc
            pytest.skip(f"cairosvg / libcairo not available: {exc}")

        from custom_components.escpos_printer.printer.svg_renderer import (
            rasterise_svg_to_png,
        )

        png = rasterise_svg_to_png(_MINIMAL_SVG, output_width=64)
        # PNG magic number — proves cairosvg produced a real raster.
        assert png.startswith(b"\x89PNG\r\n\x1a\n")
        # Silence unused-import warning while keeping the explicit
        # import as documentation of the dependency under test.
        assert cairosvg is not None


# ---------------------------------------------------------------------------
# F-T-HIGH-3 — service-chain integration: walk the full
# resolve_image_bytes → process_image_from_bytes pipeline for each
# SVG source kind (data URI, local file, HTTP). cairosvg is stubbed
# via sys.modules so libcairo is not required to run the suite.
# ---------------------------------------------------------------------------


async def _resolve_then_render(hass: Any, source: str) -> dict[str, Any]:
    """Walk resolve_image_bytes → process_image_from_bytes; return cairosvg kwargs."""
    from custom_components.escpos_printer.image_sources import (
        resolve_image_bytes,
    )

    captured: dict[str, Any] = {}

    def fake_svg2png(**kwargs: Any) -> None:
        captured.update(kwargs)
        kwargs["write_to"].write(_fake_png_bytes(64, 32))

    raw, _hint = await resolve_image_bytes(hass, source)
    with _stub_cairosvg(fake_svg2png):
        process_image_from_bytes(raw, ImageProcessOptions(width=384))
    return captured


class TestSvgServiceChain:
    async def test_data_uri_svg_walks_full_pipeline(self, hass: Any) -> None:
        uri = "data:image/svg+xml;base64," + base64.b64encode(_MINIMAL_SVG).decode()
        captured = await _resolve_then_render(hass, uri)
        assert captured["output_width"] == 384
        assert captured["output_height"] == MAX_PROCESSED_HEIGHT
        assert captured["unsafe"] is False

    async def test_local_svg_file_walks_full_pipeline(
        self,
        hass: Any,
        tmp_path: Any,
        monkeypatch: pytest.MonkeyPatch,
    ) -> None:
        path = tmp_path / "logo.svg"
        path.write_bytes(_MINIMAL_SVG)
        monkeypatch.setattr(hass.config, "is_allowed_path", lambda _p: True)
        captured = await _resolve_then_render(hass, str(path))
        assert captured["output_width"] == 384

    async def test_local_svg_rejected_at_fetch_when_above_svg_cap(
        self,
        hass: Any,
        tmp_path: Any,
        monkeypatch: pytest.MonkeyPatch,
    ) -> None:
        # F-MED-1: when the file extension is .svg, the per-read cap
        # tightens to MAX_SVG_SIZE_BYTES (1 MB) rather than the 10 MB
        # raster cap. A file larger than 1 MB must be refused at the
        # fetcher boundary (via the stat-size check in
        # ``open_local_image_no_follow``), never reaching
        # ``validate_svg_bytes``. The downstream validator's own
        # size cap is exercised separately by
        # ``TestValidateSvgBytes.test_rejects_oversized_payload``.
        from custom_components.escpos_printer.image_sources import (
            resolve_image_bytes,
        )
        from custom_components.escpos_printer.security import MAX_SVG_SIZE_BYTES

        path = tmp_path / "big.svg"
        # Write just-over the SVG cap with bytes that are NOT inside a
        # valid <svg> root, so if the fetcher ever stopped enforcing
        # the SVG cap and delegated to the validator, the validator
        # would fail with a different message (XML parse error) — which
        # would make this test fail loudly rather than silently shift
        # to exercising the wrong gate.
        path.write_bytes(b"x" * (MAX_SVG_SIZE_BYTES + 1))
        monkeypatch.setattr(hass.config, "is_allowed_path", lambda _p: True)
        with pytest.raises(HomeAssistantError, match="too large"):
            await resolve_image_bytes(hass, str(path))
