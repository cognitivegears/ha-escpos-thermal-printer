class TestValidateSvgBytes:
    def test_accepts_minimal_svg(self) -> None:
        assert validate_svg_bytes(_MINIMAL_SVG) == _MINIMAL_SVG

    def test_rejects_oversized_payload(self) -> None:
        # Pad past the cap with a benign attribute body so the size check
        # fires before the parser ever runs.
        padding = b"a" * (MAX_SVG_SIZE_BYTES + 1)
        with pytest.raises(HomeAssistantError, match="too large"):
            validate_svg_bytes(padding)

    def test_rejects_non_bytes(self) -> None:
        with pytest.raises(HomeAssistantError, match="bytes"):
            validate_svg_bytes("not bytes")  # type: ignore[arg-type]

    def test_rejects_malformed_xml(self) -> None:
        with pytest.raises(HomeAssistantError, match="Invalid or unsafe SVG"):
            validate_svg_bytes(b"<svg><not-closed>")

    def test_rejects_non_svg_root(self) -> None:
        with pytest.raises(HomeAssistantError, match="root element"):
            validate_svg_bytes(b"<html><body/></html>")

    def test_rejects_entity_expansion_billion_laughs(self) -> None:
        # Defusedxml refuses ENTITY declarations outright (EntitiesForbidden).
        # The exact wording comes from defusedxml; we just require that the
        # validator returns a HomeAssistantError rather than rendering it.
        bomb = (
            b'<?xml version="1.0"?>\n'
            b"<!DOCTYPE lolz [\n"
            b'  <!ENTITY lol "lol">\n'
            b'  <!ENTITY lol2 "&lol;&lol;&lol;&lol;&lol;">\n'
            b"]>\n"
            b'<svg xmlns="http://www.w3.org/2000/svg">&lol2;</svg>'
        )
        with pytest.raises(HomeAssistantError, match="Invalid or unsafe SVG"):
            validate_svg_bytes(bomb)

    def test_rejects_external_entity_reference(self) -> None:
        xxe = (
            b'<?xml version="1.0"?>\n'
            b'<!DOCTYPE foo [<!ENTITY xxe SYSTEM "file:///etc/passwd">]>\n'
            b'<svg xmlns="http://www.w3.org/2000/svg">&xxe;</svg>'
        )
        with pytest.raises(HomeAssistantError, match="Invalid or unsafe SVG"):
            validate_svg_bytes(xxe)

    def test_accepts_boundary_element_count(self) -> None:
        # MAX_SVG_ELEMENTS counts every node including the root, so we
        # pack ``MAX - 1`` children to land exactly at the cap.
        children = b"".join(
            b'<rect width="1" height="1"/>' for _ in range(MAX_SVG_ELEMENTS - 1)
        )
        doc = b'<svg xmlns="http://www.w3.org/2000/svg">' + children + b"</svg>"
        # No exception — exactly at the cap is acceptable.
        validate_svg_bytes(doc)

    def test_rejects_over_element_count(self) -> None:
        # MAX_SVG_ELEMENTS + 1 child elements + the root tips us over.
        children = b"".join(
            b'<rect width="1" height="1"/>' for _ in range(MAX_SVG_ELEMENTS)
        )
        doc = b'<svg xmlns="http://www.w3.org/2000/svg">' + children + b"</svg>"
        with pytest.raises(HomeAssistantError, match="too many elements"):
            validate_svg_bytes(doc)

    def test_rejects_over_use_count(self) -> None:
        uses = b"".join(
            b'<use href="#a"/>' for _ in range(MAX_SVG_USE_ELEMENTS + 1)
        )
        doc = b'<svg xmlns="http://www.w3.org/2000/svg">' + uses + b"</svg>"
        with pytest.raises(HomeAssistantError, match="<use>"):
            validate_svg_bytes(doc)

    def test_rejects_over_use_count_xlink_href(self) -> None:
        # F-T-MED-5: Inkscape and SVG 1.1 use ``xlink:href`` instead of
        # ``href``. The tag name is still ``<use>``, so the cap fires
        # the same way; this test pins that contract.
        uses = b"".join(
            b'<use xlink:href="#a"/>' for _ in range(MAX_SVG_USE_ELEMENTS + 1)
        )
        doc = (
            b'<svg xmlns="http://www.w3.org/2000/svg" '
            b'xmlns:xlink="http://www.w3.org/1999/xlink">'
            + uses
            + b"</svg>"
        )
        with pytest.raises(HomeAssistantError, match="<use>"):
            validate_svg_bytes(doc)

    def test_rejects_deeply_nested_over_element_count(self) -> None:
        # F-T-MED-4: the element-count cap must hold regardless of
        # tree shape. Flat siblings are tested above; this exercises
        # the same iter() traversal against a 5000-deep nested tree.
        # Build by string concat so we never blow Python's recursion
        # limit while constructing the test fixture.
        depth = MAX_SVG_ELEMENTS + 1
        doc = (
            b'<svg xmlns="http://www.w3.org/2000/svg">'
            + b"<g>" * depth
            + b"</g>" * depth
            + b"</svg>"
        )
        with pytest.raises(HomeAssistantError, match="too many elements"):
            validate_svg_bytes(doc)

    def test_accepts_doctype_passthrough(self) -> None:
        # F-T-MED-1 + Phase-4 verification: defusedxml's
        # ``forbid_dtd=False`` default lets ``<!DOCTYPE svg PUBLIC ...>``
        # survive the parse. Tightening to ``forbid_dtd=True`` would
        # break legitimate Inkscape exports — pin the current contract
        # so a future "harden defusedxml" change cannot silently
        # regress real-world SVGs.
        inkscape_style = (
            b'<?xml version="1.0" encoding="UTF-8" standalone="no"?>\n'
            b'<!DOCTYPE svg PUBLIC "-//W3C//DTD SVG 1.1//EN" '
            b'"http://www.w3.org/Graphics/SVG/1.1/DTD/svg11.dtd">\n'
            b'<svg xmlns="http://www.w3.org/2000/svg" width="32" height="32"/>'
        )
        # No exception — the DOCTYPE flavour Inkscape emits must pass.
        validate_svg_bytes(inkscape_style)


