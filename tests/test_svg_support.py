"""Tests for SVG support: sniffing, defusedxml validation, rasterisation hop.

The rasterisation path itself requires libcairo at runtime, which is
present on Home Assistant OS / Container but not on every dev box. The
``cairosvg`` module is therefore replaced via :data:`sys.modules` so the
pipeline can be exercised end-to-end without dlopen'ing libcairo; the
validation-only tests (sniff + defusedxml) need no system libraries.
"""

